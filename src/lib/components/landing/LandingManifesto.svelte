<section class="manifesto" id="manifesto-section">
	<div class="manifesto-inner">
		<div class="manifesto-eyebrow">Why we built this</div>

		<div class="manifesto-block">
			<p>
				<strong
					>"We wanted to give a voice to the voiceless, but what emerged was one of the most
					effective organs of Establishment propaganda in history."</strong
				>
			</p>
			<p>That's not us talking. That's Larry Sanger — the man who co-founded Wikipedia.</p>
		</div>

		<div class="manifesto-block">
			<p>
				Wikipedia has <span class="villain"
					>833 administrators, 16 bureaucrats, and 15 arbitration committee members</span
				>. Of the 62 most powerful accounts, 85% are anonymous. No one knows who these people are,
				who pays them, or what agendas they serve.
			</p>
			<p>
				Sanger suspects many of them are paid operatives. WikiScanner found edits coming from CIA
				headquarters in Langley — <strong
					>removing Iraq War casualty counts, deleting satellite images of Guantanamo Bay</strong
				>. The FBI edited articles too. As Sanger puts it:
				<em
					>"Intelligence agencies use Wikipedia as a one-stop shop to make people believe things."</em
				>
			</p>
		</div>

		<div class="manifesto-quote">
			"Wikipedia is completely dishonest and completely controlled on questions that matter. The word
			for it is propaganda."
			<cite>— Larry Sanger, co-founder of Wikipedia, Tucker Carlson Show, 2025</cite>
		</div>

		<div class="manifesto-block">
			<p>
				Since 2017, Wikipedia has maintained a <span class="villain">source blacklist</span> that
				bans Fox News, the New York Post, the Daily Caller, the Epoch Times, and The Federalist. If
				your defenders only exist in blacklisted sources, you will not be defended. The blacklist was
				created right after Trump's election. Coincidence.
			</p>
			<p>
				The COVID lab leak article asserted, in Wikipedia's own voice, that "there is no evidence
				supporting laboratory involvement" — a claim now disputed by the FBI and the Department of
				Energy. On the talk page, prior "consensus" was brandished to <span class="villain"
					>reject including updated intelligence findings</span
				>. The Gaza genocide article asserts Israel committed genocide, with almost no coverage of
				dissenting views — including those of both Wikipedia founders.
			</p>
		</div>

		<div class="manifesto-block">
			<p>
				This is not a broken process. <strong
					>This is the process working exactly as designed.</strong
				> Wikipedia's own neutrality policy says "articles must not take sides" — and then its
				consensus mechanism does nothing but pick sides, call the winner "neutral," and lock the page.
			</p>
			<p>
				We didn't build Wikifreedia because we think we're smarter than Wikipedia's editors. We built
				it because <strong>no one should have that power</strong>. Not them. Not us. Not anyone.
			</p>
		</div>

		<div class="manifesto-quote">
			"The power to declare what is known is nearly the power to rule the world. No small group — no
			person, corporation, oligarchy, or cadre of insiders — should wield such power."
			<cite>— Larry Sanger, Encyclosphere address, 2019</cite>
		</div>

		<div class="manifesto-block">
			<p>
				Knowledge isn't a single document that gets voted on. It's a living argument between people
				who see the world differently. The moment you flatten that into one "neutral" page, you
				haven't created knowledge — <span class="villain"
					>you've created propaganda with citations</span
				>.
			</p>
		</div>

		<div class="manifesto-final">
			Wikifreedia exists because <em>the disagreement is the knowledge</em>.
		</div>
	</div>
</section>
